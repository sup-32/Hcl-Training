package com.split;

public class ExtraType {
	String name;
	Long runs;
ExtraType()
{
	
}
public ExtraType(String name,Long runs )
{
	this.name=name;
	this.runs=runs;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Long getRuns() {
	return runs;
}
public void setRuns(Long runs) {
	this.runs = runs;
}

}